                <div id="divgnyd">
<h4>国内机票的预定步骤</h4>
查询航班-选择航班舱位-填写订单信息-核对价格选择支付方式-支付完成。<br/><br/>
第一步：查询航班<br/>
进入国内票查询界面，我是采购-国内采购-航班查询<br/><br/>
<img src="help/gnyd-01.png" border="0" alt="gogofly,国际机票,查询航班"/><br/><br/>
输入出到港城市，选择起飞日期<br/><br/>
<img src="help/gnyd-02.png" border="0" alt="gogofly,国际机票,选择起飞日期"/><br/><br/>
出到港城市默认为成都-上海，可以输入三字码或者城市中文名拼音首字母进行查询。日期默认第二天，点击进行更改。信息填写完成之后点击立即查询进入查询结果页面。<br/><br/>
第二步：选择航班舱位<br/>
页面跳转到查询结果列表页，点“更多”展开显示某一航班的所有舱位选择您需要的机票，点“预订”按钮，页面转入预订信息填写页面。<br/><br/>
<img src="help/gnyd-03.png" border="0" alt="gogofly,国际机票,选择航班舱位"/><br/><br/>
第三步：填写订单信息<br/>
进入订单填写页面，请您按要求正确填写乘机人信息，联系人信息，在填写乘机人信息时，请注意可点击右方常旅客进行快速填写，如果乘客不在常旅客名单内可填写完成之后点击保存为常旅客，系统自动添加常旅客信息，方便下次为该乘客预定。信息填写完成后点击创建订单，进入支付方式选择页面。<br/><br/>
<img src="help/gnyd-04.png" border="0" alt="gogofly,国际机票,填写订单信息"/><br/><br/>
第四步：选择支付方式<br/>
选择所需要的支付方式后点击立即购买，系统将自动跳转至对应支付页面。<br/><br/>
<img src="help/gnyd-05.png" border="0" alt="gogofly,国际机票,选择支付方式"/><br/><br/>
支付完成等待出票<br/><br/>
<img src="help/gnyd-06.png" border="0" alt="gogofly,国际机票,支付完成等待出票"/><br/><br/>
支付完成后点击确认操作，页面跳转至订单列表，等待出票。
