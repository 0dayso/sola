            <div id="divText" class="divText"><P align=center><STRONG><FONT size=5>高端航空需求下滑　航空公司如何“过冬”</FONT></STRONG></P>
<P align=center><FONT size=3>【来源】</FONT><A href="http://www.traveldaily.cn/article/73834.html"><FONT size=3>民航资源网</FONT></A></P>
<P align=center>&nbsp;</P>
<P><FONT size=3>&nbsp;高端航空需求是全球主要航空公司重要的收入来源，对航空公司的生存和发展至关重要。从全球范围来看，高端航空需求不振正损害着世界各地航空公司的利益。几十年来持续的需求下滑对航空业打击巨大：上世纪70年代到80年代初国际原油价格大幅上涨，80年代初、90年代初以及00年代两次的经济衰退都对航空运输需求产生了重要影响。尽管每次的经济衰退过后都会经济复苏，但是高端航空需求在每次经济衰退后都遭受了结构性的打击，很难恢复到原来的水平。如何应对高端航空需求的结构性下滑，是全球主要航空公司所共同面对的一个重要课题。</FONT></P>
<P><STRONG><FONT size=3>&nbsp;高端航空需求的特点和发展形势</FONT></STRONG></P>
<P><FONT size=3>&nbsp;1、高度周期性，与宏观经济形势正相关，且波动更大。</FONT></P>
<P><FONT size=3>&nbsp;航空运输是世界经济发展的风向标。宏观经济与航空运输需求高度正相关。经济形势向好，航空运输需求快速增长，发生经济危机，往往会导致航空需求大幅下滑。经济发展的周期性也就是航空需求的周期性，而高端需求则对经济发展的周期性有更高敏感度，波动幅度更大，在经济衰退后恢复更慢。</FONT></P>
<P><FONT size=3>&nbsp;从中国经济发展趋势和航空业发展趋势来看，两者之间呈现高度的相关性。从2000年至2011年中国国内生产总值增长376.63%，而中国民航客运量则增长436.15%（数据来源：国家统计局官网）。航空运输需求的增长趋势与经济发展的趋势非常吻合，而且航空运输业作为基础产业要超前于经济的发展。</FONT></P>
<P><FONT size=3>&nbsp;从世界经济发展和全球航空运输量来看，则更能体现出高端航空需求与经济发展趋势的正相关性，以及具有更大的波动性。以最近的2009年的全球经济衰退为例，这场经济衰退是由2008年的金融危机所引发的。下图是2007年1月-2012年12月全球旅客运输量的增长趋势。可以明显的看出经济衰退导致航空需求的大幅下滑。而这场全球的经济衰退对高端舱位和经济舱位的影响也大不相同。2009年5月高端舱位销售数量同比2008年5月大幅下降24%，但是经济舱位销售数量同比仅下降9%左右。而且高端舱位销售的下降要早于经济舱位销售的下降，因为当公司预计会发生经济衰退的时候，通常会削减出行预算。高端舱位销售情况的恢复也往往比经济舱位更晚、更慢，因为差旅部门在经济转好后仍然通常会保持谨慎态度，因为会有节约成本的压力。</FONT></P>
<P align=center><FONT size=3>&nbsp;图1：全球旅客运输量增长趋势</FONT></P>
<P align=center><FONT size=3><IMG src="http://www.380747.com/Upload/CP00000000/130828020646.jpg"></FONT></P>
<P align=center><FONT size=3>（时间段：2007年1月-2012年12月；数据来源：IATA网站）</FONT></P>
<P><FONT size=3>&nbsp;2、高端航空需求对收益影响巨大，较小的降幅将带来巨大的收益损失</FONT></P>
<P><FONT size=3>&nbsp;高端航空需求由于其销售票价水平较高，价格弹性较小，从而成为航空公司利润的重要来源。高端航空需求的小幅下降会对航空公司的收益产生巨大的影响，尤其是对全球那些拥有较多高端舱位的主流航空公司影响更大。高端舱位销售表现不佳已经导致其在整个航空运输里面的份额有所下降。在经济衰退以前，高端舱位旅客在总的航空旅客中占比约为9.5%，而在经济恢复后高端舱位旅客的占比仅从2009年低点的7.5%部分的恢复至8%。虽然市场占比下降1.5个百分点看上去不是一个很大的数字，但是这意味着高端旅客数量的降幅超过15%。</FONT></P>
<P><FONT size=3>&nbsp;进一步讲，假设高端舱位价格是普通经济舱价格的4倍，其它的因素不变（如下图所示），那么高端旅客市场占比的下降带来的收益方面的影响是非常巨大的，大概导致了4%的总收入损失。对于边际收益非常小的航空业来说，这样规模的损失是非常巨大的。</FONT></P>
<P align=center><FONT size=3>表1、高端旅客占比从9.5%下降至8%所造成的收入影响</FONT></P>
<P align=center><FONT size=3><IMG src="http://www.380747.com/Upload/CP00000000/130828020834.jpg"></FONT></P>
<P><FONT size=3>&nbsp;3、经济危机后高端旅客占比呈结构性下降趋势，后期增长乏力</FONT></P>
<P><FONT size=3>&nbsp;从2008年经济衰退前的高峰至2009年经济衰退的谷底，高端旅客占比基本呈下降趋势，如下图所示，即使在2010年经济有所复苏的情况下，高端旅客占比没有恢复至经济危机之前的水平，仍在低位徘徊。据IATA（国际航协）季度调整后的数据显示经济舱旅客的数量在此期间下降了大约7%，但是高端舱位旅客的数量下降了24%。经济舱旅客数量在2010年初恢复到经济衰退前的水平，但是高端舱位旅客数量直到2012年底才恢复到经济衰退前的水平。目前经济舱旅客数量相比2008年最高点的时候已经增长了16%左右，而高端舱位旅客数量相比2008年最高点的时候仅增长了1%左右。高端航空需求呈现结构性下滑趋势。</FONT></P>
<P align=center><FONT size=3>图2：高端旅客占比发展趋势</FONT></P>
<P align=center><FONT size=3><IMG src="http://www.380747.com/Upload/CP00000000/130828020947.jpg"></FONT></P>
<P><FONT size=3>&nbsp;4、高端客流集中于欧美航线地区航线，中东地区高端客源增长迅速</FONT></P>
<P><FONT size=3>&nbsp;全球高端客户集中于欧美航线上，欧洲内部航线以及北大西洋航线占全球高端客流总量的43%。而这两个地区由于受经济危机的影响，高端舱位销售不景气，并且北大西洋航线高端舱位销售人数和收入的份额自2010年以来均呈下降趋势。与此形成鲜明对照的是全球高端客源增长最为迅速的客流方向是从中东地区至远东地区以及从欧洲至中东地区。这对于中东地区的航空公司来说是一个非常利好的消息，而对于经营欧洲-远东直达航线的公司来说，则增加了竞争压力。</FONT></P>
<P><STRONG><FONT size=3>&nbsp;航空运输企业的应对之道</FONT></STRONG></P>
<P><FONT size=3>&nbsp;应对高端航空需求结构性下滑的趋势，航空公司应从服务提升、航线资源配置、市场营销以及合作共赢等方面采取措施，多管齐下，在不利的市场环境下获得最大的经营利润，并借由高端市场的营销提升品牌形象和整体服务水平。</FONT></P>
<P><FONT size=3>&nbsp;第一，服务体验的升级是吸引高端旅客的重要因素，但要走差异化发展路线，避免同质化竞争。高端旅客对价格敏感度较低，在服务体验（机上服务、航班准点、航班密度等）上要求较高。</FONT></P>
<P><FONT size=3>&nbsp;一是需要航空公司加大两舱硬件设施的投入，提供有亮点、有噱头的硬件设备供顾客使用，在一定程度形成产品的差异化。如新加坡航空公司在A380飞机上提供豪华套房和名厨设计的独特美食，国航推出紫轩豪华头等舱，这些都是有亮点的硬件设施，容易吸引旅客的眼球和注意力，形成独特的竞争优势。</FONT></P>
<P><FONT size=3>&nbsp;二是完善高端客户从订票到旅客到达目的地的整个服务流程的设计，珍惜每一次的服务机会，提供精准、惊喜的服务。服务流程的设计应突出精确化、简约化以及个性化，为高端旅客节约时间、节约精力，提供舒适、便捷、尊享服务。</FONT></P>
<P><FONT size=3>&nbsp;三是对高端两舱旅客的服务要有专门的团队进行管理，与高端客户建立密切联系，数个高端客户配备一个专门的客户经理，留住并开发高端客户的最大价值。对两舱旅客的个性化服务要求要尽量进行保障，大力拓展延伸服务，如酒店预订、租车、购物等项目，变单一的机票销售为出行服务套餐产品销售，做高端客户的商旅管家，为旅客提供“门到门”的服务。另外，还可在特殊日期（如生日等）为两舱旅客提供特别的服务，增强两舱旅客的忠诚度。</FONT></P>
<P><FONT size=3>&nbsp;四是细分航线，加大在公商务航线上班次的密度，大力度保障公商务航线的航班准点率，满足高端旅客对航班时刻要求较多的需求。</FONT></P>
<P><FONT size=3>&nbsp;第二，精细研究高端客源出行方向和习惯，合理配置航线资源。对于航空公司而言，应该详细分析其航线结构中高端客流较大的航线，以及哪些航线高端客流增长潜力较大。高端舱位应尽量投入高端客源较多、高端客源增长较快的航线上去。同一条航线不同时刻的航班高端客源情况也有所区别，尽量摸清高端客源习惯，将高端舱位较多的飞机投放到高端客源集中的航线时刻上，增加资源利用率。总之，精细化航线高端客流分析，优化航线高端舱位的投入将有利于高端舱位的客座率水平，实现资源利用最大化。</FONT></P>
<P><FONT size=3>&nbsp;第三，在市场营销方面，应在加大宣传和推广力度，拓展销售渠道，创新设计两舱产品等方面不断改进。高端旅客的活动范围和内容较为具有特点，因此，在宣传推广和渠道拓展方面要有别于普通舱位的销售。高端舱位的宣传推广活动应集中于高端舱位潜在购买者经常活动的高端休闲娱乐和居住场所，如高档酒店、高尔夫球场等地。只有这样才能精准营销，使宣传效果最大化。在渠道方面，除了常规渠道外，应开展全员营销，主动营销，向潜在客户推销我们的高端产品，并了解客户需求，提供个性定制化产品，满足其多样化、个性化产品需求。</FONT></P>
<P><FONT size=3>&nbsp;第四，应对需求下降的另一条途径就是适当减少供给。航空公司应对高端旅客分布进行充分调研，清楚掌握高端航空需求较少、下降较为严重并且未来高端客源发展较为缓慢的客流方向，适当减少相关航线的两舱运力投入，这样可降低成本，避免座位虚耗。阿联酋航空公司目前正在研究采用两级客舱布局的空中客车A380飞机，取消头等舱以增加更多座位。</FONT></P>
